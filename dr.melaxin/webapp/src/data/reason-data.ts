// ============================================
// Search Function
// ============================================
export function searchReasons(query: string): ReasonItem[] {
  const lowerQuery = query.toLowerCase();
  return allReasonItems.filter(
    (item) =>
      item.question.toLowerCase().includes(lowerQuery) ||
      item.answer.toLowerCase().includes(lowerQuery) ||
      item.category.toLowerCase().includes(lowerQuery) ||
      (item.details && item.details.some((d) => d.toLowerCase().includes(lowerQuery)))
  );
}

// ============================================
// Get Reasons by Category
// ============================================
export function getReasonsByCategory(categoryId: string): ReasonItem[] {
  const category = allReasonCategories.find((c) => c.id === categoryId);
  return category ? category.items : [];
}
